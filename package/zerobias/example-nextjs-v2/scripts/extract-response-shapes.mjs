// Extract the TypeScript shape of each demo's RESPONSE type from the installed SDK's `.d.ts`, so
// the app can show/copy the real, version-accurate class definition (anti-rot: never hand-authored).
//
// Run with `npm run extract:shapes` — re-run whenever `@zerobias-com/platform-sdk` bumps. Emits
// `src/lib/response-shapes.generated.ts`, a `{ TypeName: "class TypeName { … }" }` map bundled into
// the client. Mirrors the `ingest-tokens.mjs` pattern (a build-time codegen off node_modules).
//
// Scope: the immediate class body (field names + their declared types), stripped of the OpenAPI
// generator's quoted keys and its static/constructor plumbing. Nested named field types
// (ProjectStatusDef, TagView, …) are shown by name, not expanded — that transitive walk is the
// future "type explorer", not this.

import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const HERE = dirname(fileURLToPath(import.meta.url));
const APP_ROOT = join(HERE, "..");
const MODEL_DIR = join(APP_ROOT, "node_modules/@zerobias-com/platform-sdk/dist/model");
const OUT = join(APP_ROOT, "src/lib/response-shapes.generated.ts");

// The RESPONSE type each code-reveal write demo returns (verified against the platform-sdk API
// signatures): create/update Project/Board/Task -> *Extended; addComment -> TaskComment.
const TYPES = ["ProjectExtended", "BoardExtended", "TaskExtended", "TaskComment"];

/** Pull the field lines out of an OpenAPI-generated model `.d.ts` and re-emit a clean class body. */
function extractShape(typeName) {
  const src = readFileSync(join(MODEL_DIR, `${typeName}.d.ts`), "utf8");
  const open = src.indexOf(`class ${typeName} {`);
  if (open === -1) throw new Error(`class ${typeName} not found in ${typeName}.d.ts`);
  const body = src.slice(src.indexOf("{", open) + 1);

  const fields = [];
  for (const rawLine of body.split("\n")) {
    const line = rawLine.trim();
    // Stop at the generator's static/constructor plumbing — everything after is not data shape.
    if (line.startsWith("static ") || line.startsWith("constructor(") || line === "}") break;
    // Field lines look like `'name'?: Type;` — strip the quotes, keep the (optional) marker + type.
    const m = line.match(/^'([^']+)'(\??):\s*(.+);$/);
    if (m) fields.push(`  ${m[1]}${m[2]}: ${m[3]};`);
  }
  if (!fields.length) throw new Error(`no fields extracted for ${typeName}`);
  return `class ${typeName} {\n${fields.join("\n")}\n}`;
}

const shapes = Object.fromEntries(TYPES.map((t) => [t, extractShape(t)]));

const header = `// GENERATED by scripts/extract-response-shapes.mjs from @zerobias-com/platform-sdk — DO NOT EDIT.
// Regenerate with \`npm run extract:shapes\` (re-run when the SDK bumps). The shapes are the REAL
// installed-SDK class definitions, so the app's "response type" popovers can never drift.
`;
const bodyTs = `export const RESPONSE_SHAPES: Record<string, string> = ${JSON.stringify(shapes, null, 2)};\n`;

writeFileSync(OUT, `${header}\n${bodyTs}`);
console.log(`Wrote ${OUT} — ${TYPES.length} shapes: ${TYPES.join(", ")}`);
