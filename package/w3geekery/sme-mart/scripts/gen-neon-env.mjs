/**
 * Generates src/environments/environment.neon.ts from NEON_DATABASE_URL env var.
 * This file is gitignored — each developer sets their own connection string in .env.local.
 *
 * Run automatically via `prestart` npm script (dotenv-cli loads .env.local first).
 */
import { writeFileSync } from 'fs';
import { resolve, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const outPath = resolve(__dirname, '../src/environments/environment.neon.ts');

const url = process.env.NEON_DATABASE_URL || '';

const content = `// AUTO-GENERATED by scripts/gen-neon-env.mjs — DO NOT COMMIT
// Regenerated on every \`npm start\` from NEON_DATABASE_URL in .env.local
export const NEON_DATABASE_URL = '${url.replace(/'/g, "\\'")}';
`;

writeFileSync(outPath, content, 'utf-8');

if (url) {
  console.log('[gen-neon-env] Wrote Neon connection string to environment.neon.ts');
} else if (!process.env.CI) {
  // CI builds (UAT/QA/prod) don't use Neon direct mode — file is generated empty
  // and the import is replaced via angular.json fileReplacements. Only warn in
  // local dev where a missing NEON_DATABASE_URL is genuinely a misconfiguration.
  console.warn('[gen-neon-env] NEON_DATABASE_URL not set — Neon direct mode will not work');
}
